package com.example.hanaabsence;

public class EtudiantAbs {

    String nom;
    String prenom;
    int nombresAbsences;


    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public int getNombresAbsences() {
        return nombresAbsences;
    }

    public void setNombresAbsences(int nombresAbsences) {
        this.nombresAbsences = nombresAbsences;
    }
}

